-- ЗАДАЧА 4: Распределение среднего балла по дисциплинам для заданного дня недели
-- Использование: psql -d vuz_lab5 -v arg1=1 -f queries/tasks/04_avg_grade_by_subject_for_day.sql
-- где arg1 = 1 (понедельник), 2 (вторник), 3 (среда), 4 (четверг), 5 (пятница), 6 (суббота), 7 (воскресенье)

\echo '=== СРЕДНИЙ БАЛЛ ПО ДИСЦИПЛИНАМ ДЛЯ ДНЯ НЕДЕЛИ :arg1 ==='

SELECT 
    ДИСЦИПЛИНЫ.название AS "Дисциплина",
    AVG(УСПЕВАЕМОСТЬ.оценка) AS "Средний балл"
FROM УСПЕВАЕМОСТЬ
INNER JOIN ДИСЦИПЛИНЫ ON ДИСЦИПЛИНЫ.код = УСПЕВАЕМОСТЬ.дисциплина
WHERE EXTRACT(DOW FROM УСПЕВАЕМОСТЬ.дата) = :arg1
GROUP BY ДИСЦИПЛИНЫ.название
ORDER BY "Средний балл" DESC;

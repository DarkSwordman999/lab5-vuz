-- Функция 2.1: Скалярная функция - средний балл факультета
DROP FUNCTION IF EXISTS get_faculty_avg_grade(VARCHAR);

CREATE OR REPLACE FUNCTION get_faculty_avg_grade(p_faculty_name VARCHAR)
RETURNS NUMERIC(3,2) AS $$
/*
  НАЗНАЧЕНИЕ: Вычисляет средний балл всех студентов указанного факультета
  ВХОД: p_faculty_name - название факультета (например, 'Информационных технологий')
  ВЫХОД: средний балл (2.00-5.00) или NULL
  АЛГОРИТМ: JOIN ГРУППЫ и УСПЕВАЕМОСТЬ, AVG(оценка) с фильтром по факультету
*/
BEGIN
    RETURN (
        SELECT ROUND(AVG(У.оценка), 2)
        FROM УСПЕВАЕМОСТЬ У
        JOIN ГРУППЫ Г ON Г.код = У.группа
        WHERE Г.факультет = p_faculty_name
    );
END;
$$ LANGUAGE PLpgSQL;

-- Функция 2.2: Табличная функция - распределение по факультетам и сезонам
DROP FUNCTION IF EXISTS grades_by_faculty_and_season();

CREATE OR REPLACE FUNCTION grades_by_faculty_and_season()
RETURNS TABLE(
    факультет VARCHAR,
    сезон VARCHAR,
    средний_балл NUMERIC(3,2),
    количество_оценок BIGINT,
    порядок_сезона INT
) AS $$
/*
  НАЗНАЧЕНИЕ: Распределение среднего балла по факультетам и сезонам (2 признака)
  ВХОД: нет
  ВЫХОД: таблица с колонками:
    - факультет - название факультета
    - сезон - время года (осень, зима, весна, лето)
    - средний_балл - средняя оценка
    - количество_оценок - число оценок в группе
    - порядок_сезона - для сортировки (зима=1, весна=2, лето=3, осень=4)
  АЛГОРИТМ: JOIN УСПЕВАЕМОСТЬ + ГРУППЫ + СЕЗОН, GROUP BY факультет, сезон
  ОСОБЕННОСТИ: Использует ORDER BY порядок_сезона для правильной сортировки сезонов
*/
BEGIN
    RETURN QUERY
    SELECT 
        Г.факультет,
        С.сезон,
        ROUND(AVG(У.оценка), 2) AS avg_grade,
        COUNT(*) AS total_count,
        MIN(С.порядок) AS season_order
    FROM УСПЕВАЕМОСТЬ У
    JOIN ГРУППЫ Г ON Г.код = У.группа
    JOIN СЕЗОН С ON С.месяц = EXTRACT(MONTH FROM У.дата)
    GROUP BY Г.факультет, С.сезон
    ORDER BY Г.факультет, MIN(С.порядок);
END;
$$ LANGUAGE PLpgSQL;

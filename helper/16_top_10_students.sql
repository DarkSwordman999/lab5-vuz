-- Топ-10 студентов по среднему баллу
SELECT 
    СТУДЕНТЫ.фамилия || ' ' || СТУДЕНТЫ.имя AS студент,
    AVG(УСПЕВАЕМОСТЬ.оценка) AS средний_балл,
    COUNT(УСПЕВАЕМОСТЬ.оценка) AS количество_оценок
FROM УСПЕВАЕМОСТЬ
INNER JOIN СТУДЕНТЫ ON СТУДЕНТЫ.код = УСПЕВАЕМОСТЬ.студент
GROUP BY СТУДЕНТЫ.фамилия, СТУДЕНТЫ.имя
ORDER BY средний_балл DESC
LIMIT 10;

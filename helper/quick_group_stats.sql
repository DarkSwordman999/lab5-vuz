-- Статистика по группе
SELECT 
    ГРУППЫ.название AS группа,
    COUNT(DISTINCT УСПЕВАЕМОСТЬ.студент) AS студентов,
    AVG(УСПЕВАЕМОСТЬ.оценка) AS средний_балл,
    COUNT(CASE WHEN УСПЕВАЕМОСТЬ.оценка = 5 THEN 1 END) AS отлично,
    COUNT(CASE WHEN УСПЕВАЕМОСТЬ.оценка = 2 THEN 1 END) AS неуд
FROM УСПЕВАЕМОСТЬ
INNER JOIN ГРУППЫ ON ГРУППЫ.код = УСПЕВАЕМОСТЬ.группа
WHERE ГРУППЫ.название = :'group_name'
GROUP BY ГРУППЫ.название;
